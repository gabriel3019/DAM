package Bucles;

public class buclefor {

	public static void main(String[] args) {
		// Declaración de variables
		int x = 1;

		// Condición
		while (x <= 10) {
			System.out.println(x);
			// Incremento
			x++;
		}

		// 1. Declaración de variables
		// 2. Condición del bucle
		// 3. Incremento
		for (int y = 1; y <= 10; y += 2) {
			System.out.println(y);
		}

	}

}
